function swapImage()
 { 
   var num=Math.floor(Math.random() * 3) + 1;
   var photo=document.getElementById("testimonials");
   if (num == 1)
      photo.style.background="url(rotate/testimonial1.jpg) left top no-repeat";   
   else if (num == 2)
      photo.style.background="url(rotate/testimonial2.jpg) left top no-repeat";  
   else if (num == 3)
      photo.style.background="url(rotate/testimonial3.jpg) left top no-repeat";  
   else
      photo.style.background="url(rotate/testimonial4.jpg) left top no-repeat";  
   
 }
